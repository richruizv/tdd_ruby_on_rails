class Calculator

  def self.add(first, second)
    first + second
  end

  def self.substract(first, second)
    first - second
  end

  def self.multiply(first, second)
    first * second
  end

  def self.divide(first, second)
    first / second
  end

  def self.pow(first, second)
    first ** second
  end


end